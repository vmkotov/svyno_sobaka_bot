# Создаем итоговый файл
OUTPUT_FILE="svyno_sobaka_bot_full_code.txt"

{
echo "# ============================================================================"
echo "# СВИНОСОБАКА БОТ - ПОЛНЫЙ КОД ПРОЕКТА"
echo "# Собрано: $(date)"
echo "# ============================================================================"
echo ""

# Главный файл
echo ""
echo "# ============================================================================"
echo "# ФАЙЛ: main.go"
echo "# ============================================================================"
cat main.go

# Все .go файлы из mybot/ рекурсивно, отсортированные по имени
find mybot -name "*.go" -type f | sort | while read -r file; do
    echo ""
    echo "# ============================================================================"
    echo "# ФАЙЛ: $file"
    echo "# ============================================================================"
    cat "$file"
done
} > "$OUTPUT_FILE"

echo "✅ Файл создан: $OUTPUT_FILE"
echo "📊 Размер: $(wc -l < $OUTPUT_FILE) строк"
echo "📁 Файлов: $(find mybot -name "*.go" -type f | wc -l) + main.go"