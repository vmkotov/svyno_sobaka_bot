package mybot

import (
    tgbotapi "github.com/go-telegram-bot-api/telegram-bot-api/v5"
)

// CheckAllTriggers проверяет ВСЕ триггеры в порядке приоритета
// Возвращает true при первом срабатывании любого триггера
func CheckAllTriggers(bot *tgbotapi.BotAPI, msg *tgbotapi.Message, logChatID int64) bool {
    // Проверяем В ТОЧНОМ ПОРЯДКЕ ИЗ ОРИГИНАЛЬНОГО handler.go
    
    // ===============================================
    // 1. ТРИГГЕР: "ебать уфу" или "ебать спартак" (ПЕРВЫЙ приоритет)
    // ===============================================
    if CheckEbatTriggers(bot, msg, logChatID) {
        return true // Триггер сработал, дальше не проверяем
    }
    
    // ===============================================
    // 2. Проверка триггерных слов "Свинособака" (второй приоритет)
    // ===============================================
    if CheckSvinoSobakaTriggers(bot, msg, logChatID) {
        return true // Триггер сработал, дальше не проверяем
    }
    
    // ===============================================
    // 3. ТРИГГЕР: "спартак куда денем" (третий приоритет)
    // ===============================================
    if CheckHuiNadenemTriggers(bot, msg, logChatID) {
        return true // Триггер сработал, дальше не проверяем
    }
    
    // ===============================================
    // 4. Проверка триггерных слов "Спартак" (четвертый приоритет)
    // ===============================================
    if CheckSpartakTriggers(bot, msg, logChatID) {
        return true // Триггер сработал, дальше не проверяем
    }
    
    // ===============================================
    // 5. ТРИГГЕР: Алкогольные фразы (пятый приоритет)
    // ===============================================
    if CheckAlcoholTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 6. ТРИГГЕР: Капец/пиздец (шестой приоритет)
    // ===============================================
    if CheckKapetsTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 7. ТРИГГЕР: свинособака-v2 (седьмой приоритет)
    // ===============================================
    if CheckSvinoSobakaV2Triggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 8. ТРИГГЕР: долбаёб сертификат (восьмой приоритет)
    // ===============================================
    if CheckDolbaebCertificateTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 9. ТРИГГЕР: музыка → Король и Шут (девятый приоритет)
    // ===============================================
    if CheckMusicKorolShutTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 10. ТРИГГЕР: Россия → Слава России! (десятый приоритет)
    // ===============================================
    if CheckRussiaSlavaTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 11. ТРИГГЕР: китаец → 2 варианта (одиннадцатый приоритет)
    // ===============================================
    if CheckKitajecTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 12. ТРИГГЕР: иди на хуй (двенадцатый приоритет)
    // ===============================================
    if CheckIdiNaHuiTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 13. ТРИГГЕР: бар/кабак (тринадцатый приоритет)
    // ===============================================
    if CheckBarSwinobarmenTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 14. ТРИГГЕР: Виталик (четырнадцатый приоритет)
    // ===============================================
    if CheckVitalikViebatTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 15. ТРИГГЕР: вчера/помните (пятнадцатый приоритет)
    // ===============================================
    if CheckVcheraNajralsyaTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 16. ТРИГГЕР: мразь/дебил (шестнадцатый приоритет)
    // ===============================================
    if CheckMrazotstvoTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // ===============================================
    // 17. ТРИГГЕР: выезд (семнадцатый приоритет)
    // ===============================================
    if CheckVyezdPrazdnikTriggers(bot, msg, logChatID) {
        return true // Триггер сработал
    }
    
    // Ни один триггер не сработал
    return false
}
